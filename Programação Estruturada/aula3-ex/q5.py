#Escreva um programa que solicite ao usuário o seu salário mensal e o número de meses trabalhados no ano. Calcule e imprima o salário anual.
mensal = float(input('Digitel o seu salário mensal ? '))
n = int(input('Digitel número de meses trabalhados no ano ? '))
anual =  mensal * n

print('O seu salário anual é de ',anual)
