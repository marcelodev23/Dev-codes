#Escreva um programa que solicite ao usuário uma temperatura em graus Celsius e converta-a para Fahrenheit. Imprima o resultado formatado.
graus = int(input('Digitel a temperatura: '))

Fahrenheit = (graus*9/5) + 32

print('A temperatura em Celsius é',graus ,'ºC', 'convertida em Fahrenheit',Fahrenheit, '°F')



