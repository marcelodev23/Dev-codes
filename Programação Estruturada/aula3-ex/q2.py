#Escreva um programa que solicite ao usuário dois números inteiros e armazene-os em variáveis. Em seguida, calcule e imprima a soma, subtração, multiplicação e divisão desses números.
n1 = int(input('Digite um numero: '))
n2 = int(input('Digite outro numero: '))

soma =  n1 + n2
subtração = n1 - n2
multiplicação = n1 * n2
divisão = n1 / n2
print('A soma dois numeros é de',soma)
print('A subtração dois numeros é de ',subtração)
print('A multiplicação dois numero é de ',multiplicação)
print('A divisão dois numero é de ',divisão)